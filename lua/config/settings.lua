-- SETTINGS

-- map Leader
vim.g.mapleader = " "

-- Muestra los numeros de cada linea
vim.o.number = true
-- Muestra la posicion en la que se esta posicionado
vim.o.relativenumber = true

-- Desabilita la creacion de archivos swap cuando se cierra de forma inesperada el archivo
vim.o.swapfile = false

-- Habilita copy-paste
vim.o.clipboard = "unnamedplus"

-- Usar espacios en lugar de tabulaciones
vim.o.expandtab = true
-- El número de espacios que una tabulación representa
vim.o.tabstop = 4
-- El número de espacios que se utilizan para cada nivel de indentación
vim.o.shiftwidth = 4
-- El número de espacios que se utilizan cuando se presiona la tecla de tabulación en modo de inserción
vim.o.softtabstop = 4
-- Habilitar indentación inteligente
vim.o.smartindent = true

